void CGADrawChar8x8(int x, int y, int ch, int attr)
{
	int i, j;
	const unsigned char *f = &ram[0xFFA6E + ch * 8];
	unsigned char b, m;
	unsigned long fg, bg;

	fg = attr & 0x0F; //pal16[attr & 0x0F];
	bg = (attr >> 4) & 0x0F; //pal16[(attr >> 4) & 0x0F];
	for (i = 0; i < 8; i++)
	{
		for (j = 0, m = 0x80; j < 8; j++)
		{
			b = *f;
			set_pixel_2x2(x + j, y + i, b & m ? fg : bg);
			m >>= 1;
		}
		f++;
	}
}

