void update_screen_text40_color()
{
	int i, j, k;
	const unsigned char *p = &ram[0xb8000];
	int cx = ram[0x450];
	int cy = ram[0x451];
	int blink = GetTickCount() % 1000 < 500;
	for (i = 0; i < 25; i++)
	{
		for (j = 0; j < 40; j++)
		{
			CGADrawChar8x8(j * 8, i * 8, p[0], p[1]);
			if ((i == cy) && (j == cx))
				if (blink)
					for (k = 0; k < 8; k++)
						set_pixel(k + j * 8, i * 8 + 7, pal16[p[1] & 0x0F]);
			p += 2;
		}
	}
}

