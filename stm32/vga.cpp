unsigned int vga_logic(unsigned int v, unsigned int mask)
{
	switch (vga_logic_op)
	{
		case 0: return (v & mask) | (vga_latch.d & (~mask));
		case 1: return (v | (~mask)) & vga_latch.d;
		case 2: return (v & mask) | vga_latch.d;
		case 3: return (v & mask) ^ vga_latch.d;
	}
	return v;
}

